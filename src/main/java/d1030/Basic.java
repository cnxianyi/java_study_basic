package d1030;


/*
* 八个基本类型
* */
public class Basic {
    public static void main(String[] args) {
        boolean b = true; // 1
        byte by = 1; // 8
        char c = 'a'; // 16
        short s = 1; // 16
        int i = 1; // 32
        float f = 1.1f; // 32
        long l = 1; // 64
        double d = 1.2d; // 64

        System.out.println(b);
        System.out.println(by);
        System.out.println(c);
        System.out.println(s);
        System.out.println(i);
        System.out.println(f);
        System.out.println(l);
        System.out.println(d);
    }
}
